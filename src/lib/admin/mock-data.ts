export function getPropertyById(id: string): AdminProperty | undefined {
  return adminProperties.find((p) => p.id === id);
}

export function getInvestmentsByProperty(propertyId: string): PropertyInvestment[] {
  return propertyInvestments.filter((i) => i.propertyId === propertyId);
}

export function getUserById(id: string): AdminUser | undefined {
  return adminUsers.find((u) => u.id === id);
}

export function getActivitiesByUser(userId: string): UserActivity[] {
  return userActivities.filter((a) => a.userId === userId);
}
